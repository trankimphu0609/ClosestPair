package Entity;

/**
 * @author Trần Kim Phú
 */
public class Point {

    public double x;
    public double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + ((int) x) + "," + (int) y + ")";
    }
}
